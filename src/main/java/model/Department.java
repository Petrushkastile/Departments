package model;

public class Department {

    private int id;
    private String name;
    private int countEmploees;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCountEmploees() {
        return countEmploees;
    }

    public void setCountEmploees(int countEmploees) {
        this.countEmploees = countEmploees;
    }

}
